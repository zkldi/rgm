pub fn get_gravity(level: i32) -> i32 {
	match level {
		500.. => 5120,
		450.. => 768,
		420.. => 1024,
		400.. => 1280,
		360.. => 1024,
		330.. => 768,
		300.. => 512,
		251.. => 256,
		247.. => 224,
		243.. => 192,
		239.. => 160,
		236.. => 128,
		233.. => 96,
		230.. => 64,
		220.. => 32,
		200.. => 4,
		170.. => 144,
		160.. => 128,
		140.. => 112,
		120.. => 96,
		100.. => 80,
		90.. => 64,
		80.. => 48,
		70.. => 32,
		60.. => 16,
		50.. => 12,
		40.. => 10,
		35.. => 8,
		30.. => 6,
		_ => 4,
	}
}

pub fn grav_to_rpf(grav: i32) -> (i32, i32) {
	let frames: f32 = 256.0 / grav as f32;

	if frames < 1.0 {
		// every 1 frame, move us down 1/f rows
		// for frames = 0.5, this converts it into
		// (2 rows, 1 frame).
		((1.0 / frames) as i32, 1)
	} else {
		(1, frames as i32)
	}
}
